package Sept.ex_16092024;

public class Lab037_Compound_Assignment_Operator {
    public static void main(String[] args) {
        //  Compound Assignment Operators
        int a = 10;
        // += ,-= , *=, /=
        a += 20; // a = a+ 20
        System.out.println(a);

        a -= 20; // a = a - 20
        System.out.println(a);

        a /= 10; // a = a/10
        System.out.println(a);

        a*=10; // a = a*10
        System.out.println(a);

    }
}
