package Sept.ex_20292024;

public class Lab065 {
    public static void main(String[] args) {
        // Max, Min between two numbers.
        int Digambar_Salary = 7;
        int Anita_Salary = 4;

        int max = Digambar_Salary > Anita_Salary ? Digambar_Salary : Anita_Salary;
        System.out.println(max);
        int min = Digambar_Salary < Anita_Salary ? Digambar_Salary : Anita_Salary;
        System.out.println(min);
    }
}
