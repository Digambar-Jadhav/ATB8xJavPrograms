package Sept.ex_25092024;

public class Lab110 {
    public static void main(String[] args) {
        //while loop
        //while ->A,B, C
        //A-Initialization
        //B-Condition
        //C-Increament/Decreament
        int i = 0; //A
        while (i <= 10){ //B
            System.out.println(i);
            i++; //C
          }
        for (int j = 0; j <=10 ; j++) {
            System.out.println(j);
        }

    }
}
